// Utility functions
export const utils = {
  // Get color with optional alpha
  getColor: (colorPath: string, alpha?: number) => {
    const pathParts = colorPath.split('.');
    let color: any = colors;
    
    for (const part of pathParts) {
      color = color[part];
      if (!color) return colorPath;
    }
    
    if (alpha !== undefined && typeof color === 'string') {
      // Convert hex to rgba if alpha is provided
      const hex = color.replace('#', '');
      const r = parseInt(hex.substr(0, 2), 16);
      const g = parseInt(hex.substr(2, 2), 16);
      const b = parseInt(hex.substr(4, 2), 16);
      return `rgba(${r}, ${g}, ${b}, ${alpha})`;
    }
    
    return color;
  },
  
  // Get responsive value
  getResponsiveValue: (value: string | Record<string, string>, breakpoint?: string) => {
    if (typeof value === 'string') return value;
    
    if (breakpoint && value[breakpoint]) {
      return value[breakpoint];
    }
    
    // Return base value or first available
    return value.base || value[Object.keys(value)[0]];
  },
  
  // Create focus ring styles
  getFocusRing: (highContrast = false) => ({
    outline: 'none',
    boxShadow: `0 0 0 ${accessibility.focusRingWidth} ${
      highContrast ? accessibility.highContrastFocusRingColor : accessibility.focusRingColor
    }`,
    borderRadius: borderRadius.md,
  }),
  
  // Create high contrast styles
  getHighContrastStyles: () => ({
    color: colors.highContrast.text,
    backgroundColor: colors.highContrast.background,
    border: `1px solid ${colors.highContrast.border}`,
  }),
};
